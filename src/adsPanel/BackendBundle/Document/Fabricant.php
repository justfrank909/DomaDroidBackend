<?php

namespace adsPanel\BackendBundle\Document;



/**
 * adsPanel\BackendBundle\Document\Fabricant
 */
class Fabricant
{

    /**
     * Get id
     *
     * @return id $id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return \Fabricant
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * Get name
     *
     * @return string $name
     */
    public function getName()
    {
        return $this->name;
    }
}